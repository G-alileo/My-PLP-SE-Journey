student_info = {"name": "Charlie", "age": 21, "grade": "A"}

# This is a simple dictionary to hold student information
print("Student Information:", student_info)  # Outputs: {'name': 'Charlie', 'age': 21, 'grade': 'A'}

# Accessing a value by its key
print("Student Name : ",student_info["name"])  # Outputs: Charlie

# Modifying a value
student_info["age"] = 22
print("Modified student info  : ",student_info)  # Outputs: {'name': 'Charlie', 'age': 22, 'grade': 'A'}