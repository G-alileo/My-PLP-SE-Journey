fruits = ["apple", "banana", "cherry"]

# This is a simple list of fruits
print("Original list of fruits:", fruits)  # Outputs: ['apple', 'banana', 'cherry']

# Accessing elements
print("First item in the list : ", fruits[0])  # Outputs: apple

# Modifying an element
fruits[1] = "orange"
print("Modifying the second element in the list : ",fruits)  # Outputs: ['apple', 'orange', 'cherry']

# Adding an element
fruits.append("grape")
print("Adding an item to the list: ",fruits)  # Outputs: ['apple', 'orange', 'cherry', 'grape']